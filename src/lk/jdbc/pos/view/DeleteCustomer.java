    private void idTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idTextActionPerformed
        String id=idText.getText();

        String SQL="Select * from Customer where id='"+id+"'";

        try {
            Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"Driver not found");
        }
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/ThogaKade","root","ijse");
            Statement statement = connection.createStatement();
            ResultSet rst = statement.executeQuery(SQL);
            if(rst.next()){
                nameText.setText(rst.getString("name"));
                addressText.setText(rst.getString("address"));
                salaryText.setText(rst.getString("salary"));
            }else{
                JOptionPane.showMessageDialog(this,"No such custmer");
            }
        } catch (SQLException ex) {
            Logger.getLogger(DeleteCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_idTextActionPerformed

