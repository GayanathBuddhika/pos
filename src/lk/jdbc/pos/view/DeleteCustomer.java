    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        String id=idText.getText();

        String SQL="Delete from Customer where id='"+id+"'";

        try {
            Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"Driver not found");
        }
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/ThogaKade","root","");
            Statement statement = connection.createStatement();
            int res = statement.executeUpdate(SQL);
            if(res>0){
                JOptionPane.showMessageDialog(this,"Deleted..");
            }else{
                JOptionPane.showMessageDialog(this,"Delete Failed");
            }
        } catch (SQLException ex) {
            Logger.getLogger(DeleteCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_deleteButtonActionPerformed

